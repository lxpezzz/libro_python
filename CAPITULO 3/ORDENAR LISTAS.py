## 
#Para ordenar listas de forma alfabeticamente se utiliza el sort()
#Para ordenar listas de forma alfabeticamente inverso se utiliza el sort(reverse=true)
##

##
#Para ordenar listas pero que mantenga el orden inicial se utilizará sorted()
##
#EJEMPLO: 
cars = ['bmw', 'audi', 'honda']

print("\nEsta es la lista inicial.")
print(cars)

print("\nEsta es la lista ordenada.")
print(sorted(cars))

print("\nEsta vuelve a ser la lista inicial.")
print(cars)

